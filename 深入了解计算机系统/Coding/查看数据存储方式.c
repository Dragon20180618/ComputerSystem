#include <stdio.h>
#include <malloc.h>
int main()
{
    int x = 128;
    char *y = (char *)&x; //对x进行强制类型转换，取其地址
    printf("%x\n", *y); //输出80,之前的值默认用f填充
    x = 64;
    printf("%x\n", *y); //输出（10）16*4=0x(40)，之前的值默认用0填充
    x = 258;
    printf("%x\n", *y); //输出2，256因为数据存储采用小端法，所以被后置，输出y[1]=可得00000001
}
//%x 表示以16进制进行输出
//可知一个字长为两个字节，最大为ff